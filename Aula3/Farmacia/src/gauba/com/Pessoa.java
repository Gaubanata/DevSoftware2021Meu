package gauba.com;

public class Pessoa {
private String nome;
private String cpf;
private String rg;

public void setNome(String nome) {
	this.nome = nome;
}
public String getNome() {
	return this.nome;
}


public void setCpf(String cpf) {
	this.cpf = cpf;
}
public String getCpf() {
	return this.cpf;
}


public void setRg(String rg) {
	this.rg = rg;
}
public String getRg() {
	return this.rg;
}

}





